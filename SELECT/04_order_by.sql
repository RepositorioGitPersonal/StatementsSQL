SELECT * FROM Users ORDER BY age;

-- La sentencia ORDER BY nos sirve para ordenar los datos, de la forma en que le pidamos. Por defecto el ORDER BY ordena los datos de menor a mayor, pero esto puede cambiar, de esta forma.

SELECT * FROM Users ORDER BY age ASC;

-- De esta forma los estamos ordenando ascendentemente, es decir, de menor a mayor.

SELECT * FROM Users ORDER BY age DESC;

-- De esta forma los estamos ordenando descendentemente, es decir, de mayor a menor.